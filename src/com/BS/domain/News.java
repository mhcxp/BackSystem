package com.BS.domain;

public class News {
	public int newsId;
	public String newsTitle;
	public String newsPath;
	public String rep;
	public String newsTime;
	public String link;
	public int phoAmo;
	public String phoPath;
	
	public int getNewsId() {
		return newsId;
	}
	public void setNewsId(int newsId) {
		this.newsId = newsId;
	}
	public String getNewsTitle() {
		return newsTitle;
	}
	public void setNewsTitle(String newsTitle) {
		this.newsTitle = newsTitle;
	}
	public String getNewsPath() {
		return newsPath;
	}
	public void setNewsPath(String newsPath) {
		this.newsPath = newsPath;
	}
	public String getRep() {
		return rep;
	}
	public void setRep(String rep) {
		this.rep = rep;
	}
	public String getNewsTime() {
		return newsTime;
	}
	public void setNewsTime(String newsTime) {
		this.newsTime = newsTime;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public int getPhoAmo() {
		return phoAmo;
	}
	public void setPhoAmo(int phoAmo) {
		this.phoAmo = phoAmo;
	}
	public String getPhoPath() {
		return phoPath;
	}
	public void setPhoPath(String phoPath) {
		this.phoPath = phoPath;
	}
}
