package com.BS.domain;

public class User {

	int id;
	String username;
	String password;
	String userNickName;
	String userMail;
	boolean isName;
	String userPhoneNum;
	String userRealName;
	Integer userLength;
	Integer userWeight;
	String userHobby;
	String userLoca;
	String userCard;
	float userMon;
	
	
	public String getUserNickName() {
		return userNickName;
	}
	public void setUserNickName(String userNickName) {
		this.userNickName = userNickName;
	}
	public String getUserMail() {
		return userMail;
	}
	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public boolean getIsName() {
		return isName;
	}
	public void setIsName(boolean isName) {
		this.isName = isName;
	}
	public String getUserPhoneNum() {
		return userPhoneNum;
	}
	public void setUserPhoneNum(String userPhoneNum) {
		this.userPhoneNum = userPhoneNum;
	}
	public String getUserRealName() {
		return userRealName;
	}
	public void setUserRealName(String userRealName) {
		this.userRealName = userRealName;
	}
	public Integer getUserLength() {
		return userLength;
	}
	public void setUserLength(Integer userLength) {
		this.userLength = userLength;
	}
	public Integer getUserWeight() {
		return userWeight;
	}
	public void setUserWeight(Integer userWeight) {
		this.userWeight = userWeight;
	}
	public String getUserHobby() {
		return userHobby;
	}
	public void setUserHobby(String userHobby) {
		this.userHobby = userHobby;
	}
	public String getUserLoca() {
		return userLoca;
	}
	public void setUserLoca(String userLoca) {
		this.userLoca = userLoca;
	}
	public String getUserCard() {
		return userCard;
	}
	public void setUserCard(String userCard) {
		this.userCard = userCard;
	}
	public float getUserMon() {
		return userMon;
	}
	public void setUserMon(float userMon) {
		this.userMon = userMon;
	}
	
	
}
