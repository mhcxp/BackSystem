package com.BS.domain;

public class Health {
	public String actName;
	public String txtPath;
	public int phoAmount;
	public String phoPath;
	
	
	public String getActName() {
		return actName;
	}
	public void setActName(String actName) {
		this.actName = actName;
	}
	public String getTxtPath() {
		return txtPath;
	}
	public void setTxtPath(String txtPath) {
		this.txtPath = txtPath;
	}
	public int getPhoAmount() {
		return phoAmount;
	}
	public void setPhoAmount(int phoAmount) {
		this.phoAmount = phoAmount;
	}
	public String getPhoPath() {
		return phoPath;
	}
	public void setPhoPath(String phoPath) {
		this.phoPath = phoPath;
	}

}
